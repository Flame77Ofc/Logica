// Estruturas de condição
// Servem para tomar decisões
// Se, senão, caso, escolha

// If (se)
let idade = 23
if (idade >= 18) {
    console.log('Você tem mais de 18 anos')
}

// Else (se não)
let nome = 'Antônio'
if (nome == 'Lucas') {
    console.log('Oi, Lucas!')
} else {
    console.log('Olá, suspeito... -_-')
}

// Else if (senão se)
let pontos = 7
if (pontos >= 10) {
    console.log('Muitos pontos!')
} else if (pontos >= 6) {
    console.log('Pontos legais!')
} else {
    console.log('Poucos pontos')
}

